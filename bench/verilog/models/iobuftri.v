module iobuftri
  (
   input  wire  i,
   input  wire oe,
   output wire o,
   inout  wire pad
   );

   assign #1 pad = oe ? i : 1'bz;
   
  assign #1    o =  pad || pad;
   
endmodule // iobuftri

