module timing_gen 
#(parameter DELAY=10,
  parameter WIDTH=20
 )
 (input wire  clk,
  input wire  reset,
  output reg  tgen
 );
   
initial
  begin
  tgen = 1'b0;
  end


always@(posedge clk  )  tgen  = #DELAY  1'b1;
always@(posedge tgen )  tgen  = #WIDTH  1'b0;


   
   


   

endmodule



